           отдельно (например, из Releases репозитория ДВИЖКА лаунчера на GitHub), настроен
           только "под движок вообще", не под конкретный сервер/модпак — сам по себе не
           заработает, лаунчер покажет "не настроен под модпак". appsettings.json рядом с exe
           уже содержит нужные адреса — именно эта пара файлов и есть то, что тебе нужно.
        2. Положи оба файла (exe + appsettings.json) в отдельную, пустую папку — не в
           System32 и не в папку с игрой. Не удаляй и не переименовывай appsettings.json —
           лаунчер читает его из своей же папки при запуске.
        3. Запусти exe.

        Ничего дополнительно ставить не нужно — сборка самодостаточная. Дальнейшие обновления
        самого exe (не модпака) лаунчер после этого умеет накатывать сам, кнопкой "Проверить
        обновление лаунчера" — appsettings.json при этом не трогается, только сам exe.

        ## Первый запуск
        Лаунчер попросит указать папку сборки — это твой Minecraft-профиль с модами (обычно
        внутри лежат подпапки mods и config; это папка инстанса в CurseForge/твоём лаунчере
